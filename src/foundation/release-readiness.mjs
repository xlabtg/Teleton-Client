function clone(value) {
  return structuredClone(value);
}

export function listReleaseReadinessChecklist() {
  return clone(RELEASE_READINESS_CHECKLIST);
}

export function listReleaseDocumentationInventory() {
  return clone(RELEASE_DOCUMENTATION_INVENTORY);
}

export function validateReleaseReadinessChecklist(checklist = RELEASE_READINESS_CHECKLIST) {
  const errors = [];

  if (!Array.isArray(checklist)) {
    return ['Release readiness checklist must be an array.'];
  }

  const ids = checklist.map((item) => item.id);
  if (JSON.stringify(ids) !== JSON.stringify(RELEASE_READINESS_REQUIRED_IDS)) {
    errors.push(`Release readiness checklist must include ${RELEASE_READINESS_REQUIRED_IDS.join(', ')} in order.`);
  }

  for (const item of checklist) {
    const label = item?.id ?? '<unknown>';

    if (item?.requiredBeforePublicRelease !== true) {
      errors.push(`${label} must be required before public release.`);
    }

    if (!item?.humanApproverRole) {
      errors.push(`${label} must name a human approver role.`);
    }

    if (!Array.isArray(item?.evidence) || item.evidence.length === 0) {
      errors.push(`${label} must list release review evidence.`);
    }

    if (!Array.isArray(item?.documents) || item.documents.length === 0) {
      errors.push(`${label} must link release review documents.`);
    }
  }

  return errors;
}

export function validateReleaseDocumentationInventory(inventory = RELEASE_DOCUMENTATION_INVENTORY) {
  const errors = [];

  if (!Array.isArray(inventory)) {
    return ['Release documentation inventory must be an array.'];
  }

  for (const entry of inventory) {
    const label = entry?.id ?? '<unknown>';

    if (!entry?.source) {
      errors.push(`${label} must declare the source of truth.`);
    }

    if (!entry?.test) {
      errors.push(`${label} must declare validation coverage.`);
    }

    if (!Array.isArray(entry?.docs) || entry.docs.length === 0) {
      errors.push(`${label} must declare documentation coverage.`);
      continue;
    }

    for (const doc of entry.docs) {
      if (!doc?.path || !(doc.pattern instanceof RegExp)) {
        errors.push(`${label} documentation entries must include a path and RegExp pattern.`);
      }
    }
  }

  return errors;
}

export function assertReleaseReadinessChecklist({
  checklist = RELEASE_READINESS_CHECKLIST,
  inventory = RELEASE_DOCUMENTATION_INVENTORY
} = {}) {
  const errors = [
    ...validateReleaseReadinessChecklist(checklist),
    ...validateReleaseDocumentationInventory(inventory)
  ];

  if (errors.length > 0) {
    throw new Error(`Invalid release readiness checklist:\n- ${errors.join('\n- ')}`);
  }

  return true;
}
